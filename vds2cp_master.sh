#!/bin/bash

# -----------------------------------------------------------------------------
# get a list of VDS accounts on the server.
# -----------------------------------------------------------------------------

IFS=$'\n'
vdsusrlist=($(cat /etc/passwd|grep sphera|grep -vE 'sd.*admn'|awk -F':' '{print $1}'))
# echo "${my_array[1]}"
unset IFS

# -----------------------------------------------------------------------------
#
# -----------------------------------------------------------------------------

for i in "${vdsusrlist@]}";do eval mkdir -p "~$i/root/";done
